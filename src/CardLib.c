//--------------------------------------------------------------------
//  手札の表示
//--------------------------------------------------------------------

void card_show(int cd[], int n) 
{
  int k;
  for ( k = 0; k < n; k++ ) {
    if ( cd[k] < 0 ) { continue; }
    printf("%s ", card_str(cd[k]));
  }
}

//--------------------------------------------------------------------
//  手札の表示 ログへの保存
//--------------------------------------------------------------------

void card_show_log(FILE *fp, int cd[], int n) 
{
  int k;
  for ( k = 0; k < n; k++ ) {
    if ( cd[k] < 0 ) { continue; }
    fprintf(fp, "%s ", card_str(cd[k]));
  }
}


