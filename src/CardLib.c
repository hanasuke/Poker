//--------------------------------------------------------------------
//  山札の生成
//--------------------------------------------------------------------

void card_stock(int stock[], FILE *fp) 
{
  char line[CNUM*3];
  char * pt;
  int card;
  int i = 0;
  
  pt = fgets(line, CNUM*3, fp);
  if ( pt == NULL ) {
    puts("山札の読込に失敗しました。");
    exit(EXIT_FAILURE);
  }
  while ( *pt != '\n' && *pt != '\0' ) {
    card = strtol(pt, &pt, 10);
//printf("%d\n", card);
    stock[i++] = card;
  }
}


