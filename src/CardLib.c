//--------------------------------------------------------------------
//  カードの表示子
//--------------------------------------------------------------------

char *card_str(int id) 
{
  char *str ="  ";
  str[0] = SUITE[id / 13];
  str[1] = NUMBER[id % 13];
  str[2] = '\0';
  return str;
}

//--------------------------------------------------------------------
//  カードの識別子
//--------------------------------------------------------------------

int card_id(char *str) 
{
  int num;
  int sut;
  int k;
  for ( k = 0; k < 4; k++ ) { 
    if ( SUITE[0] == str[0] ) { sut = k; }
  }
  for ( k = 0; k < 13; k++ ) { 
    if ( NUMBER[0] == str[0] ) { num = k; }
  }
  return sut * 13 + num;
}


